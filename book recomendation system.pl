    book(Book, _, Year, _, _, _, Mood, Genre, Era, Situation),
    mood_genre(Mood, Genre),
    era_match(Year, Era),
    situation_match(Book, Situation).

% Rule to find recommendations based on user input
find_recommendation(Mood, Genre, Era, Situation) :-
    recommend(Book, Mood, Genre, Era, Situation),
    book(Book, Author, Year, SellingRate, Abstract, Articles, _, _, _, _),
    writeln('We recommend the following book based on your preferences:'),
    format('Title: ~w\n', [Book]),
    format('Author: ~w\n', [Author]),
    format('Year: ~w\n', [Year]),
    format('Selling Rate: ~w\n', [SellingRate]),
    format('Abstract: ~w\n', [Abstract]),
    format('Articles: ~w\n', [Articles]),
    !.

find_recommendation(_, _, _, _) :-
    writeln('Sorry, no book matches your preferences.').

