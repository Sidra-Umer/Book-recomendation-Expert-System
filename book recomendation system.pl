% Rule to display recommendations
display_recommendations([]) :-
    writeln('No books match your criteria. Please refine your choices and try again.').

display_recommendations(Books) :-
    length(Books, Count),
    format('Here are ~w recommended books:\n', [Count]),
    display_books(Books, 1).

% Helper rule to display books
display_books([], _).
display_books([Book|Rest], Index) :-
    format('~w. ~w\n', [Index, Book]),
    NextIndex is Index + 1,
    display_books(Rest, NextIndex).

% Rules for recommending books based on criteria
